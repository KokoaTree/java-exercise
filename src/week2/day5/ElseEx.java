package week2.day5;

public class ElseEx {
    public static void main(String[] args) {
        boolean checkTrue = true;
        if(checkTrue) {
            System.out.println("true입니다.");
        } else {
            System.out.println("false입니다.");
        }

        boolean checkFalse = false;
        if(checkFalse) {
            System.out.println("false입니다.");
        } else {
            System.out.println("true입니다.");
        }
    }
}
