public class ElseEx3 {
    public static void main(String[] args) {
        int score = 81;
        if (score >= 90) {
            System.out.println("A");
        } if (score >= 80) {
            System.out.println("B");
        } if (score >= 70) {
            System.out.println("C");
        }
    }
}
