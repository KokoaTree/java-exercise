package week2.day3;

public class ArithmeticOperators1 {
    public static void main(String[] args) {
        int num1 = 3;
        int num2 = 10;

        System.out.printf("a + b = %d\n", num1 + num2);
        System.out.printf("a - b = %d\n", num1 - num2);
        System.out.printf("a * b = %d\n", num1 * num2);
        System.out.printf("a / b = %d\n", num1 / num2);
        System.out.printf("a %% b = %d\n", num1 % num2);

    }
}
