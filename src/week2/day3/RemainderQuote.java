package week2.day3;

public class RemainderQuote {
    public static void main(String[] args) {
        int iVal = 1;

        System.out.println(iVal / 2);
        System.out.println();

        int quote = 10 / 3;
        int remainder = 10 % 3;

        System.out.printf("10을 3으로 나눈 몫은 %d, 나머지는 %d이다.", quote, remainder);

    }
}
